    <h2>Innebygde funksjoner</h2>
    <h4>Spørsmål:</h4>
    <p>Hva er en funksjon?</p>
    <ul> 
        <li>En funksjon er en gjenbrukbar kode som utfører en spesifikk oppgave.</li>
    </ul> 
    <p>Hva er en innebygd funksjon?</p>
    <ul>
        <li>En innebygd funksjon er en funksjon som er forhåndsdefinert i programmeringsspråket og kan brukes uten å måtte definere den selv.</li>
    </ul>
    <p>Hvor finnes dokumentasjon på funksjonsbiblioteket?</p>
    <ul>
        <li>Dokumentasjon på funksjonsbiblioteket finnes vanligvis i den offisielle dokumentasjonen finnes på php.net.</li>  
    </ul>
    <p>Hva er en parameter?</p>
    <ul>
        <li>En parameter er en variabel som brukes i funksjonsdefinisjonen for å motta verdier når funksjonen kalles.</li>  
    </ul>
    <p>Hva menes med en standarverdi for en parameter?</p>
    <ul>
        <li>En standardverdi for en parameter er en forhåndsdefinert verdi som brukes hvis ingen verdi blir gitt når funksjonen kalles.</li>    
    </ul>
    <p>Hva er en funksjon returverdi?</p>
    <ul>
        <li>En funksjon returverdi er verdien som funksjonen returnerer etter at den har utført sin oppgave.</li>   
    </ul>
    <p>Hvordan definerer du en egenutviklet funksjon?</p>
    <ul>
        <li>En egenutviklet funksjon defineres ved å bruke nøkkelordet "function" etterfulgt av funksjonsnavnet, parenteser for eventuelle parametere, og en kodeblokk som inneholder funksjonens logikk.</li>
    </ul>
    <p>Hva er en variabels arbeidsområde når den benyttes i en funksjon?</p>
    <ul>
        <li>En variabels arbeidsområde (scope) i en funksjon refererer til hvor variabelen er tilgjengelig. Variabler definert inne i en funksjon er lokale for den funksjonen og kan ikke nås utenfor den.</li>
    </ul>   
    <p>Hvordan kan du dynamisk kalle en funksjon?</p>
    <ul>
        <li>Du kan dynamisk kalle en funksjon ved å bruke variabler som inneholder funksjonsnavnet, eller ved å bruke funksjoner som "call_user_func()" i PHP.</li> 
    </ul>
    <p>Hva er Unix Epoch?</p>
    <ul>
        <li>Unix Epoch er tidspunktet 00:00:00 UTC den 1. januar 1970, som brukes som referansepunkt for tidsberegning i mange datamaskinsystemer.</li> 
    </ul>
